    public void botonIgual(){
        double numero2;
        if(operacion=="nula"){
            etiquetaES.setText(cadenaNumeros);
        }
        else if(operacion=="suma"){
            numero2= Double.parseDouble(cadenaNumeros);
            resultado= numero + numero2;         
            etiquetaES.setText(String.format("%.2f" , resultado));
            cadenaNumeros= String.valueOf(resultado);
            operacion="nula";         
        }
        else if(operacion=="resta"){
            numero2= Double.parseDouble(cadenaNumeros);
            resultado= numero - numero2;          
            etiquetaES.setText(String.format("%.2f" , resultado));
            cadenaNumeros= String.valueOf(resultado);
            operacion="nula";
        }
        else if(operacion=="division"){
            numero2= Double.parseDouble(cadenaNumeros);
            resultado= numero / numero2;        
            etiquetaES.setText(String.format("%.2f" , resultado));
            cadenaNumeros= String.valueOf(resultado);
            operacion="nula";
        }
        else if(operacion=="multiplicacion"){
            numero2= Double.parseDouble(cadenaNumeros);
            resultado= numero * numero2;          
            etiquetaES.setText(String.format("%.2f" , resultado));//Solo mostrar dos decimales
            cadenaNumeros= String.valueOf(resultado);//Obtener un string de un double
            operacion="nula";
        }
        
        etiquetaHistorial.setText("");  
        activado=true;
    }

