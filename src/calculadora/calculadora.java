    private void botonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarActionPerformed
        try{
            if(cadenaNumeros.charAt(cadenaNumeros.length()-1)=='.'){
            punto=true;
            }
            if(cadenaNumeros!=""){
                if(cadenaNumeros.length() == 1){
                    etiquetaES.setText("0");
                    cadenaNumeros= cadenaNumeros.substring(0, cadenaNumeros.length()-1);//Borrar el ultimo caracter de una cadena de texto
                    activado=false;
                }
                else{
                    cadenaNumeros= cadenaNumeros.substring(0, cadenaNumeros.length()-1);
                    etiquetaES.setText(cadenaNumeros);
                }           
            }
        } catch(StringIndexOutOfBoundsException si){
            
        }  
    }//GEN-LAST:event_botonBorrarActionPerformed

